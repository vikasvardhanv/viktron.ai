// Helper functions
export const getAllNiches = (): Niche[] => {
  return FORGE_INDUSTRY_CATEGORIES.flatMap((category) => category.niches);
};

export const getNichesByCategory = (categoryId: string): Niche[] => {
  const category = FORGE_INDUSTRY_CATEGORIES.find((cat) => cat.id === categoryId);
  return category ? category.niches : [];
};

export const getCategoryById = (categoryId: string): IndustryCategory | undefined => {
  return FORGE_INDUSTRY_CATEGORIES.find((cat) => cat.id === categoryId);
};

export const searchNiches = (query: string): Niche[] => {
  const lowerQuery = query.toLowerCase();
  return getAllNiches().filter(
    (niche) =>
      niche.name.toLowerCase().includes(lowerQuery) ||
      niche.category.toLowerCase().includes(lowerQuery)
  );
};
